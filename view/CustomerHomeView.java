    private void DataView() {
        DefaultTableModel model = new DefaultTableModel();
        userId = app.currentUser;
        if (type == 0) {
            model.addColumn("Code Item");
            model.addColumn("Owner");
            model.addColumn("Cost");
            model.addColumn("Location");

            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            if (app.store.getAmountSeller() > 0) {
                for (int i = 0; i < app.store.getAmountSeller(); i++) {
                    if (app.store.getSeller(i).getAmountHouse() > 0) {
                        for (int j = 0; j < app.store.getSeller(i).getAmountHouse(); j++) {
                            if (app.store.getSeller(i).getHouseByIdx(j).getAvailable() == true) {

                                Object[] data = {
                                    app.store.getSeller(i).getHouseByIdx(j).getCode(),
                                    app.store.getSeller(i).getUserName(),
                                    app.store.getSeller(i).getHouseByIdx(j).getCost(),
                                    app.store.getSeller(i).getHouseByIdx(j).getLocation(),};
                                model.addRow(data);
                            }
                        }
                    }
                }
            }
        } else {
            model.addColumn("Code Item");
            model.addColumn("Owner");
            model.addColumn("Cost");
            model.addColumn("Location");

            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);

            if (app.store.getAmountSeller() > 0) {
                for (int i = 0; i < app.store.getAmountSeller(); i++) {
                    if (app.store.getSeller(i).getAmountLand() > 0) {
                        for (int j = 0; j < app.store.getSeller(i).getAmountLand(); j++) {
                            if (app.store.getSeller(i).getLandByIdx(j).getAvailable() == true) {
                                Object[] data = {
                                    app.store.getSeller(i).getLandByIdx(j).getCode(),
                                    app.store.getSeller(i).getUserName(),
                                    app.store.getSeller(i).getLandByIdx(j).getCost(),
                                    app.store.getSeller(i).getLandByIdx(j).getLocation(),};
                                model.addRow(data);
                            }
                        }
                    }

                }
            }
        }

        tblOutput.setModel(model);
    }

