    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        // TODO add your handling code here:
        if (type == 0) {
            int amountCost = Integer.parseInt(inputCost.getText());
            int idHouse = Integer.parseInt(inputId.getText());
            int buildArea = Integer.parseInt(inputBuildingArea.getText());
            int manyRooms = Integer.parseInt(inputManyRooms.getText());
            String location = inputLocation.getText();
            if (app.store.getCustomer(userId).getTabungan().
                    ambilUang(amountCost)) {
                app.store.getSeller(idOwner).deleteHouse(idItem);

                app.store.getSeller(idOwner).getTabungan()
                        .simpanUang(amountCost);

                app.store.getCustomer(userId).addHouse(idHouse,
                        buildArea, manyRooms, amountCost,
                        location);
                JOptionPane.showMessageDialog(this, "Withdraw"
                        + " Success!");

            } else {
                JOptionPane.showMessageDialog(this, "Failed! You dont "
                        + "have enough money");
            }

        } else {
            int amountCost = Integer.parseInt(inputCost.getText());
            int idLand = Integer.parseInt(inputId.getText());
            int landArea = Integer.parseInt(inputBuildingArea.getText());
            String location = inputLocation.getText();
            if (app.store.getCustomer(userId).getTabungan().
                    ambilUang(amountCost)) {
                app.store.getSeller(idOwner).getHouseByIdx(idItem).
                        setAvailable(false);
                app.store.getSeller(idOwner).getTabungan()
                        .simpanUang(amountCost);

                app.store.getCustomer(userId).addLand(idLand,
                        landArea, amountCost, location);
                JOptionPane.showMessageDialog(this, "Withdraw"
                        + " Success!");

            } else {
                JOptionPane.showMessageDialog(this, "Failed! You dont "
                        + "have enough money");
            }
        }
        DataView();
        viewBalance();
    }//GEN-LAST:event_btnBuyActionPerformed

