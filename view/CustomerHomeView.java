    private void tblOutputMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOutputMouseClicked
        // TODO add your handling code here
        if (type == 0) {
            inputId.setText(tblOutput.getValueAt(tblOutput.
                    getSelectedRow(), 0).toString());
            inputOwner.setText(tblOutput.getValueAt(tblOutput.
                    getSelectedRow(), 1).toString());

            try {
                idItem = app.store.findHouse(Integer.parseInt(
                        inputId.getText()));
                idOwner = app.store.getIndexCustomerAndSeller(
                        inputOwner.getText());
                inputCost.setText(Integer.toString(app.store.
                        getSeller(idOwner).getHouseByIdx(idItem).
                        getCost()));
                inputCost.setText(Integer.toString(app.store.
                        getSeller(idOwner).getHouseByIdx(idItem).
                        getCost()));
                inputLocation.setText(app.store.
                        getSeller(idOwner).getHouseByIdx(idItem).
                        getLocation());
                inputBuildingArea.setText(Integer.toString(app.store.
                        getSeller(idOwner).getHouseByIdx(idItem).
                        getBuildingArea()));
                inputManyRooms.setText(Integer.toString(app.store.
                        getSeller(idOwner).getHouseByIdx(idItem).
                        getManyRooms()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Can't find "
                        + "id Item or id Owner");
            }

        } else {
            inputId.setText(tblOutput.getValueAt(tblOutput.
                    getSelectedRow(), 0).toString());
            inputOwner.setText(tblOutput.getValueAt(tblOutput.
                    getSelectedRow(), 1).toString());

            try {
                idItem = app.store.findLand(Integer.parseInt(
                        inputId.getText()));
                idOwner = app.store.getIndexCustomerAndSeller(
                        inputOwner.getText());
                inputCost.setText(Integer.toString(app.store.
                        getSeller(idOwner).getLandByIdx(idItem).
                        getCost()));
                inputCost.setText(Integer.toString(app.store.
                        getSeller(idOwner).getLandByIdx(idItem).
                        getCost()));
                inputLocation.setText(app.store.
                        getSeller(idOwner).getLandByIdx(idItem).
                        getLocation());
                inputBuildingArea.setText(Integer.toString(app.store.
                        getSeller(idOwner).getLandByIdx(idItem).
                        getLandArea()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Can't find "
                        + "id Item or id Owner");
            }
        }
    }//GEN-LAST:event_tblOutputMouseClicked

